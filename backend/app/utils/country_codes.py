# Reverse mapping: code to name
COUNTRY_CODE_TO_NAME = {code: name for name, code in COUNTRY_NAME_TO_CODE.items()}


def get_country_name(code: str) -> str:
    """
    Get country name from ISO 3166-1 alpha-3 code.

    Args:
        code: 3-letter country code (e.g., "USA", "DEU")

    Returns:
        Country name or the code itself if not found
    """
    return COUNTRY_CODE_TO_NAME.get(code.upper(), code)


def get_country_code(name: str) -> str | None:
    """
    Get ISO 3166-1 alpha-3 code from country name.

    Args:
        name: Country name (e.g., "United States", "Germany")

    Returns:
        3-letter country code or None if not found
    """
    return COUNTRY_NAME_TO_CODE.get(name)


def get_all_country_names() -> list[str]:
    """Get sorted list of all country names."""
    return sorted(COUNTRY_NAME_TO_CODE.keys())


def get_all_country_codes() -> list[str]:
    """Get sorted list of all country codes."""
    return sorted(COUNTRY_CODE_TO_NAME.keys())


def format_country_dropdown_option(name: str, code: str) -> str:
    """
    Format country for dropdown display.

    Args:
        name: Country name
        code: Country code

    Returns:
        Formatted string like "Germany (DEU)"
    """
    return f"{name} ({code})"


def parse_country_from_dropdown(option: str) -> tuple[str | None, str | None]:
    """
    Parse country name and code from dropdown option.

    Args:
        option: String like "Germany (DEU)" or "All Countries"

    Returns:
        Tuple of (name, code) or (None, None) if "All Countries"
    """
    if option == "All Countries" or option == "All":
        return None, None

    # Extract code from parentheses
    import re
    match = re.match(r'^(.+?)\s*\(([A-Z]{3})\)$', option)
    if match:
        return match.group(1), match.group(2)

    # Fallback: assume it's just a name
    code = get_country_code(option)
    return option, code
